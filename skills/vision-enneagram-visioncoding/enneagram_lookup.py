def dump_type(primary: int, wing: int = None, mode: str = "bible") -> dict:
    """
    유형 단독 텍스트 룩업.
    mode: 'bible' (성경) | 'secular' (역사적 인물)
    wing: 있으면 윙 조합 텍스트 포함
    """
    out = {
        "primary": primary,
        "wing": wing,
        "behavior": BEHAVIOR_PATTERN[primary],
        "healthy": HEALTHY_STATE[primary],
        "unhealthy": UNHEALTHY_STATE[primary],
        "growth_path": GROWTH_PATH[primary],
        "caution": CAUTION_KO,
        "background_mode": mode,
    }
    if mode == "bible":
        out["background"] = BIBLE_BACKGROUND[primary]
    elif mode == "secular":
        out["background"] = SECULAR_BACKGROUND[primary]
    else:
        out["error"] = f"unknown mode: {mode}"
    if wing is not None:
        combo = f"{primary}w{wing}"
        out["wing_combination_key"] = combo
        out["wing_combination_text"] = WING_COMBO.get(combo, f"⚠ 유효하지 않은 윙 조합: {combo}")
    return out


def main():
    if len(sys.argv) < 2:
        print("사용법: enneagram_lookup.py <primary> [wing] [bible|secular]")
        sys.exit(1)
    primary = int(sys.argv[1])
    wing = None
    mode = "bible"
    for arg in sys.argv[2:]:
        if arg in ("bible", "secular"):
            mode = arg
        else:
            try:
                wing = int(arg)
            except ValueError:
                pass
    if primary < 1 or primary > 9:
        print(json.dumps({"error": f"유형은 1~9, 입력: {primary}"}, ensure_ascii=False))
        sys.exit(1)
    print(json.dumps(dump_type(primary, wing, mode), ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
